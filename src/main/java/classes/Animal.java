package main.java.classes;


public class Animal {
	
	private String name;
	private String species;
	private String food;
	
	public String getName() {
		return name;
	}

	public String getSpecies() {
		return species;
	}

	public String getFood() {
		return food;
	}
	
	
	public Animal(String name) {
		
		this.name = name;
	}
	
}
